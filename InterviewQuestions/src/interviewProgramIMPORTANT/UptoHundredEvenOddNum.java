package interviewProgramIMPORTANT;

public class UptoHundredEvenOddNum {

	public static void main(String[] args) {
	
	///upto 100 even numbers
		
		for(int i=0;i<=100;i++)
		{
			if(i%2==0)
			System.out.print(i + " ");
		}
		
    /////upto 100 odd numbers
		
		for(int j=0;j<=100;j++)               
		{
			if(j%2!=0)
			System.out.print(j + " ");
		}
		
	}

}
